//: ## Type Parameters
/*:
 In the `swapTwoValues(_:_:)` example previously, the placeholder `T` is an example of a _type parameter_. Type parameters specify and name a placeholder type, and are written immediately after the function's name, between a pair of matching angle brackets (such as `<T>`).
 
 Once you specify a type parameter, you can use it to define the type of function's parameters (such as the `a` and `b` parameters of the `swapTwoValues(_:_:)` function), or as the function's return type, or as the type annotation within body of the function. In each case, the type parameter is replaced with an _actual_ type whenever the function is called. (In the `swapTwoValues(_:_:)` example previously, `T` was replaced with `Int` the first time the function was called, and was replaced with `String` the second time it was called.)
 
 You can provide more than one type parameter by writing multiple type parameter names within the angle brackets, separated by commas.
 
 Move on to [Naming Type Parameters](@next)
 */
