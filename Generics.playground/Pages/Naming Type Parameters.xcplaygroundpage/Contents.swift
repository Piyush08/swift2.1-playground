//: ## Naming Type Parameters
/*:
 In most cases, type parameters have descriptive names, such as `Key` and `Value` in `Dictionary<Key, Value>` and `Element` in `Arrray<Element>`, which tells the reader about the relationship between the type parameter and the generic type or function it's used in. However, when there isn't a meaningful relationship between them, it's traditional to name them using single letters such as `T`, `U`, such as `T` in the `swapTwoValues(_:_:)` function above.
 
 NOTE: Always give type parameters upper cammel case names (such as `T` and `MyTypeParameter`) to indicate that they are a placeholder for a _type_, not a value.
 */
